    private void jbtn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_eliminarActionPerformed
        int rut;
        rut = Integer.parseInt(this.jtxt_eliminar.getText());
        
        Registro reg = new Registro();
        
        int opcion = JOptionPane.showConfirmDialog(this, "Â¿Seguro desea eliminar la cita?", "AVISO", 1);
         if (opcion == 0){
             reg.eliminar(rut);
             JOptionPane.showMessageDialog(this, "Cita Eliminada", "AVISO", 1);
             
         }else{
             JOptionPane.showMessageDialog(this, "Cita NO Eliminada", "AVISO", 1);
         }
    }//GEN-LAST:event_jbtn_eliminarActionPerformed

